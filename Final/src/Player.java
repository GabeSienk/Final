
public class Player
	{
		private String name;
		private String award;

		public Player(String n, String a)
			{
				name = n;
				award = a;

			}

		public String getName()
			{
				return name;
			}

		public void setName(String name)
			{
				this.name = name;
			}

		public String getAward()
			{
				return award;
			}

		public void setAward(String award)
			{
				this.award = award;
			}

		

	}
